/*
    unshift = adiciona um item no inicio do array
    push = adiciona um item no final do array

*/

let arr = [1, 2, 3, 4, 5];
//exemplo basico onde seleciono o indice e coloco mais uma valor no array
arr[5] = 6
console.log(arr)

arr.push(7)
console.log(arr)
//push adiciona no final do array


arr.unshift(0)
console.log(arr)
//unshift adiciona no inicio do array

